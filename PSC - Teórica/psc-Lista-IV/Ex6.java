public class Ex6 {
    public static void main(String[] args){
        System.out.println("Numeros um embaixo do outro: ");
        int i = 0;
        while(i < 20){
            System.out.println(i+1);
            i++;
        }
        System.out.println("Numeros um ao lado do outro: ");
        i = 0;
        while(i < 20){
            System.out.print(i+1 + " ");
            i++;
        }
        System.out.println("\nE isso '-' ...");


    }
}
